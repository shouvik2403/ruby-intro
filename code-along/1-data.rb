# In Ruby, there are different *types* of data:

# Numbers
puts 2
puts 2 + 5

# Non-numbers
puts "Hello World"

# True, False
puts true
puts false

# Nothing
puts nil

# Variables
x = 5
y = 2
puts x * y

favourite_food = "tacos"
puts favourite_food

# Combine strings and variables
number_of_tacos = 5
puts "#{number_of_tacos} #{favourite_food} is good"

# String manipulation
puts favourite_food.upcase
